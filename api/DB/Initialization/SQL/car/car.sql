CREATE TABLE Car (
    id INT(11) AUTO_INCREMENT PRIMARY KEY,
    ref VARCHAR(255) DEFAULT '' UNIQUE,
    brand INT NOT NULL,
    model INT NOT NULL,
    motor INT NOT NULL,
    price INT NOT NULL,
    kilometers INT NOT NULL,
    initial_registration DATE NOT NULL,
    description TEXT DEFAULT '',
    seller INT(11) NOT NULL,
    img LONGBLOB DEFAULT '',
    createdBy INT(11) NOT NULL,
    deletedBy INT(11),
    createdAt DATETIME NOT NULL,
    updatedAt DATETIME NOT NULL,
    deletedAt DATETIME,
    FOREIGN KEY (seller) REFERENCES Seller(id) ON DELETE CASCADE,
    FOREIGN KEY (brand) REFERENCES Brand(id) ON DELETE NO ACTION,
    FOREIGN KEY (model) REFERENCES Model(id) ON DELETE NO ACTION,
    FOREIGN KEY (motor) REFERENCES Motor(id) ON DELETE NO ACTION,
    FOREIGN KEY (createdBy) REFERENCES User(id) ON UPDATE CASCADE ON DELETE NO ACTION,
    FOREIGN KEY (deletedBy) REFERENCES User(id) ON UPDATE CASCADE ON DELETE NO ACTION
);